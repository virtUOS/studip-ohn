<h1>OHN-KursPortal</h1>

<img src="<?= $GLOBALS['OHN_IMAGES'] ?>/ohn-logo_portal.png">

<p>Das OHN-KursPortal bietet beruflich qualifizierten Studieninteressierten mit
und ohne Abitur die Möglichkeit, sich mithilfe von Online-Vorbereitungskursen
gezielt auf ein Hochschulstudium vorzubereiten. Die Vorbereitungskurse
unterstützen Ihren individuellen Entscheidungsprozess, helfen, Ihren
vorhandenen Kenntnisstand einzuschätzen zu können und dienen der
zielorientierten Auffrischung und  Erweiterung von  Kenntnissen, die Sie für
ein Studium benötigen.</p>

<P>Dieses innovative Angebot bietet die Möglichkeit direkt vom heimischen PC
an interaktiven Online-Studienvorbereitungskursen teilzunehmen. Dabei werden
die Teilnehmenden durch Tutorinnen und Tutoren auch zu Einzelfragen beraten und
informiert.</p>

<p>Die Online-Studienvorbereitungskurse des OHN-KursPortals

<ul>
    <li>sind speziell auf die Bedarfe von beruflich Qualifizierten
    zugeschnitten</li>

    <li>sind so aufgebaut, dass auch unterschiedlichste Lerntypen den
    Kursinhalten gut folgen können</li>

    <li>sind klar gegliedert und einfach zu bedienen</li>

    <li>werden von erfahrenen Tutorinnen und Tutoren begleitet</li>

    <li>bieten die Möglichkeit, sich innerhalb des Kurses mit anderen
    Personen, die sich für ein Studium interessieren, auszutauschen und
    gemeinsam zu lernen</li>

    <li>ermöglichen zeit- und raumunabhängiges Lernen </li>

    <li>sind barrierearm konzipiert.</li>
</ul>
</p>
